public class BitwiseOperator {
    public static void main(String[] args) {

        // Bitwise OR
        System.out.println(5 | 9);
        // Bitwise AND
        System.out.println(5 & 9);
        // Bitwise XOR
        System.out.println(5 ^ 9);

        // left Shift
        System.out.println(5 << 2);
        // Signed Right Shift
        System.out.println(5 >> 2);
        // Unsigned Right Shift
        System.out.println(-5 >>> 2);

        // Bitwise Complement
        System.out.println(~4);
    }
}
